#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name:  print_number
//  Description:    Displays numbers till number entered by user.
//  Input:          Integer
//  Output:         Void
//  Author:         Mrunmai Jitendra Khadpe
//  Date:           24/10/20025
//
///////////////////////////////////////////////////////////////////////////////////////////////////////

void print_number(int limit)
{
    int iCnt=0;
    if(limit<0)
    {
        limit=-limit;
    }
    for(iCnt=1; iCnt<=limit; iCnt++)
    {
        printf("%d\t",iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Time Complexity: O(n)
//
///////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int limit;

    printf("Enter number: ");
    scanf("%d",&limit);

    print_number(limit);

    return 0;
}